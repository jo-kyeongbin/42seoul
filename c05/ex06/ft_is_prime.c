int	ft_is_prime(int nb)
{
	int i;
	int count;

	i = nb;
	count = 0;
	if (nb <= 1)
		return (0);
	while (i > 0)
	{
		if (nb % i == 0)
			count++;
		if (count >= 3)
			return (0);
		i--;
	}
	return (1);
}
