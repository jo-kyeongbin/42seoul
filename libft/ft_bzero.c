#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*addr;

	addr = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		addr[i] = 0;
		i++;
	}
}
